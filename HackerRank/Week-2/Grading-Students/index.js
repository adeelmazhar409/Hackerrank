function lonelyinteger(a) {
  let elementCounts = {};
  
  // Count the occurrences of each element
  a.forEach(value => {
    if (!elementCounts[value]) {
      elementCounts[value] = 1; 
    } else {
      elementCounts[value]++;
    }
  });

  // Filter out elements that appear more than once
  let uniqueElements = a.filter(value => elementCounts[value] === 1);
  
  return uniqueElements;
}

